    	//display the submit button when all questions are answered
    	$(this).change(function(){
			if (radios.filter(':checked').length >= 4) {
	    		$('.submission').show().addClass('animated bounceInRight');
	    	} else {
	    		$('.submission').hide();
	    	}
    	});
    });

